#include "lcd_CN.h"
uc16 ZN16x16_Table[] = {
	 0x08, 0x20, 0x00, 0xfc, 0x00, 0x00, 
    0xff, 0xfe, 0x7f, 0x00, 0x03, 0xfe, 
    0x08, 0x20, 0x22, 0x08, 0xfc, 0x20, 
    0x1f, 0xf0, 0x11, 0x10, 0x10, 0x40, 
    0x01, 0x00, 0x7f, 0xfc, 0x11, 0xfc, 
    0x7f, 0xfc, 0x44, 0x44, 0x21, 0x04, 
    0x01, 0x00, 0x7f, 0xfc, 0x3d, 0x04, 
    0x1f, 0xf0, 0x00, 0x08, 0x65, 0x24, 
    0x11, 0x10, 0x7e, 0x08, 0x65, 0x24, 
    0x1f, 0xf0, 0x42, 0xfe, 0xa5, 0x24, 
    0x11, 0x10, 0x7e, 0x08, 0x25, 0x24, 
    0x1f, 0xf0, 0x42, 0x48, 0x25, 0x44, 
    0x01, 0x00, 0x7e, 0x28, 0x3c, 0x50, 
    0x3f, 0xf8, 0x48, 0x08, 0x24, 0x88, 
    0x01, 0x00, 0x74, 0x28, 0x21, 0x04, 
    0xff, 0xfe, 0x42, 0x10, 0x02, 0x04
};
static  vu16 TextColor = 0x0000, BackColor = 0xFFFF;
void cout(int Xpos,int Ypos){
	int index = 0, i = 0;
	u8 Xaddress = 0;
   
	Xaddress = Xpos;
	LCD_SetCursor(Xaddress, Ypos);
  
	for(index = 0; index < 32*3; index++)
	{
		LCD_WriteRAM_Prepare(); /* Prepare to write GRAM */
		for(i = 7; i >=0; i--)
		{
			if((ZN16x16_Table[index] & (1 << i)) == 0x00)
			{
				LCD_WriteRAM(BackColor);
			}
			else
			{
				LCD_WriteRAM(TextColor);
			}
		}
		if(index%6==5)Ypos+=40;
		else Ypos-=8;
		if(index%6==5)Xaddress++;
		LCD_SetCursor(Xaddress, Ypos);
	}
}