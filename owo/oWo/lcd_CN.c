static  vu16 TextColor = 0x0000, BackColor = 0xFFFF;
void cout(int Xpos,int Ypos,int pos){
	int index = 0, i = 0;
	u8 Xaddress = 0;
   
	Xaddress = Xpos;
	LCD_SetCursor(Xaddress, Ypos);
  
	for(index = 0; index < 32; index++)
	{
		LCD_WriteRAM_Prepare(); /* Prepare to write GRAM */
		for(i = 0; i <=7; i++)
		{
			if((ZN16x16_Table[pos][index] >>i&1) == 0x00)
				LCD_WriteRAM(BackColor);
			else LCD_WriteRAM(TextColor);
		}
		if(index%2==1)Ypos+=8;
		else Ypos-=8;
		if(index%2==1)Xaddress++;
		LCD_SetCursor(Xaddress, Ypos);
	}
}